    private boolean nameIsValid(String n)
    {
        for (int i = 0; i < n.length(); i++)
        {
            char a = n.charAt(i);
            if (a < 48 || (a > 57 && a < 65) || (a > 90 && a < 97) || (a > 123))
                return false;
        }

        return true;
    }

